    probs = c(.9, rep(.005, threshold), rep(.0000001, q-threshold))
    
    # "delta" is chosen randomly from the range {0, 1, ..., q} based on the probabilities specified in "probs".
    delta <- sample(x = 0:q, prob = probs, size  = 1, replace = TRUE) 
